import UIKit

extension UIColor {
    open class var VKblue: UIColor {
        get {
              return UIColor(red: 59.0/255.0, green: 97.0/255.0, blue: 151.0/255.0, alpha: 1)
        }
    }
    
    open class var VKgray: UIColor {
        get {
            return UIColor(red: 232.0/255.0, green: 236.0/255.0, blue: 233.0/255.0, alpha: 1)
        }
    }
}
