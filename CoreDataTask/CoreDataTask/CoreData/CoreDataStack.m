        NSLog(@"Unresolved error %@, %@", error, error.userInfo);
        abort();
    }
}

+ (CoreDataStack *)defaultStack
{
    static CoreDataStack* coreData = nil;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        coreData = [CoreDataStack new];
    });
    return coreData;
}

@end
